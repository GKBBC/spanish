let score = 0;
let total = 0;

let random = vocab4b[Math.floor(Math.random()*vocab4b.length)];

function checkWords() {
    for(let i = 0; i < random[0].length; ++i) {
        if(random[0][i].trim() == document.getElementById("spanish").value.toLowerCase().trim()) {
            document.getElementById("spaSpan").innerHTML = "✔️";
            score += 0.5;
        }
    }

    for(let i = 0; i < random[1].length; ++i) {
        if(random[1][i].trim() == document.getElementById("english").value.toLowerCase().trim()) {
            document.getElementById("engSpan").innerHTML = "✔️";
            score += 0.5;
        }
    }

    if(document.getElementById("spaSpan").innerHTML == "⬜") {
        document.getElementById("spaSpan").innerHTML = "❌"
    }

    if(document.getElementById("engSpan").innerHTML == "⬜") {
        document.getElementById("engSpan").innerHTML = "❌"
    }

    if(document.getElementById("spaSpan").innerHTML == "❌" || document.getElementById("engSpan").innerHTML == "❌") {
        document.getElementById("face").src = "images/no.png";
    }
    else {
        document.getElementById("face").src = "images/yay.png";
    }

    document.getElementById("correctsp").innerHTML = random[0][0];
    document.getElementById("correcten").innerHTML = random[1][0];
    document.getElementById("next").classList.remove("gone");

    total += 1;

    document.getElementById("score").innerHTML = score + "/" + total + " (" + (Math.round((score/total) * 100)) + "%)";
    return false;
}

function newVocab() {
    let chapterName = document.getElementById("chapter").value
    let vocab = eval(chapterName);
    
    random = vocab[Math.floor(Math.random()*vocab.length)];
    document.getElementById("audioSpa").src = random[2];
    document.getElementById("audioSpa").play();
}

function reset() {
    document.getElementById("next").classList.add("gone");

    document.getElementById("spanish").value = "";
    document.getElementById("english").value = "";

    document.getElementById("spaSpan").innerHTML = "⬜";
    document.getElementById("engSpan").innerHTML = "⬜";
    
    document.getElementById("correctsp").innerHTML = "&nbsp;";
    document.getElementById("correcten").innerHTML = "&nbsp;";
    
    document.getElementById("face").src = "images/neutral.png";

    newVocab();
}
